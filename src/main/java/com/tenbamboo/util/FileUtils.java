	public static void copyFileUsingFileChannels(File source, File dest) throws IOException {    
        FileChannel inputChannel = null;    
        FileChannel outputChannel = null;    
	    try {
	        inputChannel = new FileInputStream(source).getChannel();
	        outputChannel = new FileOutputStream(dest).getChannel();
	        outputChannel.transferFrom(inputChannel, 0, inputChannel.size());
	    } finally {
	        inputChannel.close();
	        outputChannel.close();
	    }
	}
	
	public static String getFileCode(File file) throws IOException {
		String fileCode;
		String fileMd5 = MD5Util.getMD5(file);
		fileCode = fileMd5 + file.length();
		return fileCode;
	}
	
	public static File createFile(String fileName) {
		File file = new File(fileName);
		File forder = file.getParentFile();
		if (!forder.exists()) {
			forder.mkdirs();
		}
		return file;
	}
	
	//图片转化成base64字符串  
    public static String imageToBase64(String filePath)  
    {//将图片文件转化为字节数组字符串，并对其进行Base64编码处理  
        String imgFile = filePath;//待处理的图片  
        InputStream in = null;  
        byte[] data = null;  
        //读取图片字节数组  
        try   
        {  
            in = new FileInputStream(imgFile);          
            data = new byte[in.available()];  
            in.read(data);  
            in.close();  
        }   
        catch (IOException e)   
        {  
            e.printStackTrace();  
        }  
        //对字节数组Base64编码  
        BASE64Encoder encoder = new BASE64Encoder();  
        return encoder.encode(data);//返回Base64编码过的字节数组字符串  
    }  
      
    //base64字符串转化成图片  
    public static boolean base64ToImage(String imgStr, String filePath)  
    {   //对字节数组字符串进行Base64解码并生成图片  
        if (imgStr == null) //图像数据为空  
            return false;  
        BASE64Decoder decoder = new BASE64Decoder();  
        try   
        {  
            //Base64解码  
            byte[] b = decoder.decodeBuffer(imgStr);  
            for(int i=0;i<b.length;++i)  
            {  
                if(b[i]<0)  
                {//调整异常数据  
                    b[i]+=256;  
                }  
            }  
            //生成jpeg图片  
            String imgFilePath = filePath;//新生成的图片  
            OutputStream out = new FileOutputStream(imgFilePath);      
            out.write(b);  
            out.flush();  
            out.close();  
            return true;  
        }   
        catch (Exception e)   
        {  
            return false;  
        }  
    }  
    
    public static String readFile(String filePath) throws UnsupportedEncodingException, IOException {
    	String fileContent = "";
    	//读取文件(缓存字节流)
        BufferedInputStream in = new BufferedInputStream(new FileInputStream(filePath));
        //读取数据
        //一次性取多少字节
        byte[] bytes = new byte[2048];
        //接受读取的内容(n就代表的相关数据，只不过是数字的形式)
        int n = -1;
        //循环取出数据
        while ((n = in.read(bytes,0,bytes.length)) != -1) {
            //转换成字符串
            String str = new String(bytes,0,n,"UTF-8");
            fileContent += str;
            System.out.println(str);
        }
        //关闭流
        in.close();
        
        return fileContent;
    }
    
